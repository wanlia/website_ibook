<h3>Book Returns/Money Collection</h3>
Come to the iBU store to pick up your cheque and/or textbooks. If your books aren't sold, we return them to you NO CHARGE. If your textbooks are sold, you will receive 80% of the profit (based on the prices you set) and the other 20% will be donated to the BC Children's Hospital.<br />
